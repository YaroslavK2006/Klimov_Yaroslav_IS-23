S = input("Введите текст: ")
result = " ".join(S) # Создаем новую строку result в которой символы из строки S будут разделены одним пробелом Метод join() применяется к строке S и принимает ее символы в качестве списка а затем объединяет их с помощью разделителя, указанного в виде строки " "
print(result)
